#define _CRT_SECURE_NO_WARNINGS
#define MOD 15746
#define ARRAY_LEN 1000001
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

// N = 1  1   |  0  
// N = 2  2   |  00, 11  
// N = 3  3   |  001, 100, 111
// N = 4  5   |  0000, 0011, 1001, 1100, 1111
// N = 5  8   |  00001, 00100, 10000, 00111, 10011, 11001, 11100, 11111
// N = 6  13  |  000000, 000011, 001100, 110000, 100001, 001001, 100100, 001111, 111100, 110011, 100111, 111001, 111111
// N = 7  21  |  0000001, 0000100, 0010000, 1000000, 0000111, 0011100, 1110000, 0010011, 0011001, 1001100, 1100100, 1110000, 1100001, 1000011,
//				 0011111, 1001111, 1100111, 1110011, 1111001, 1111100, 1111111

int arr[ARRAY_LEN];

int main(void) {	
	int N;
	
	arr[1] = 1;
	arr[2] = 2;

	scanf("%d", &N);

	for (int i = 3; i <= N; i++) {
		arr[i] = (arr[i - 1] + arr[i - 2]) % MOD;
	}

	printf("%d", arr[N]);

	return 0;
}